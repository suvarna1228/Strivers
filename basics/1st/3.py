class Solution:
    def whichWeekDay(self, day):
        if day== 1:
            print("Monday")
        elif day== 2:
            print("Tusday")
        elif day== 3:
            print("Wednesday")
        elif day==4:
            print("Thursday")
        elif day==5:
            print("Friday")
        elif day==6:
            print("Saturday")
        else:
            print("Sunday")